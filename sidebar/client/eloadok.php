    <p>1986. október 24-én született a kanadai Torontóban. Apja Dennis Graham afroamerikai dobos, anyja Sandi Graham zsidó származású kanadai. Végzettségét tekintve pedagógus. Drake a helyi zsidó iskolában tanult. A rapper ötéves volt, amikor szülei elváltak. 2012-ben érettségizett le, miután tinédzser korában kicsapták az iskolából. Drake iskolás éveiről úgy nyilatkozott, hogy sokat bántották bőrszíne és vallása miatt.</p>
    <p>Színészként kezdte karrierjét. A Degrassi gimi című tinisorozatban mutatkozott meg először, a 2000-es évek elején.

Első szólóalbuma, a Thank Me Later 2010-ben debütált. Az album meglehetősen sikeresnek bizonyult, az amerikai Billboard 200-as listáján az élre került. 2011-ben adta ki a Take Care című stúdióalbumát, valamint 2013-ban Nothing Was the Same-et, utóbbi háromszoros platinalemez lett.

Drake negyedik stúdióalbuma, a Views 2016-ban jelent meg. Ezzel az albummal felállította rekordját Michael Jacksonnal és Whitney Houstonnal szemben is: a Billboard Hot 100-as és 200-as listáján nyolc hétig volt listavezető. Drake többek között olyan előadókkal működött együtt, mint Rihanna (What's My Name, Work), Big Sean, Kanye West (Blessings) Lil Wayne, Chris Brown, valamint Nicki Minaj.</p>

